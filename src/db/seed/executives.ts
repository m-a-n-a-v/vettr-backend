/**
 * Seed executives into the database.
 * Looks up stock IDs by ticker and inserts 3-5 executives per stock.
 * Skips stocks that are not found in the database.
 */
export async function seedExecutives(): Promise<number> {
  if (!db) {
    console.warn('‚ö†Ô∏è  Database not available - skipping executive seed');
    return 0;
  }

  console.log('üå± Seeding executives...');

  let count = 0;
  for (const stockData of executiveSeedData) {
    // Look up stock ID by ticker
    const stockRows = await db
      .select({ id: stocks.id })
      .from(stocks)
      .where(eq(stocks.ticker, stockData.ticker))
      .limit(1);

    if (stockRows.length === 0) {
      console.warn(`‚ö†Ô∏è  Stock ${stockData.ticker} not found - skipping executives`);
      continue;
    }

    const stockId = stockRows[0].id;

    for (const exec of stockData.executives) {
      await db.insert(executives).values({
        stockId,
        name: exec.name,
        title: exec.title,
        yearsAtCompany: exec.yearsAtCompany,
        previousCompanies: exec.previousCompanies,
        education: exec.education,
        specialization: exec.specialization,
        socialLinkedin: exec.socialLinkedin,
        socialTwitter: exec.socialTwitter,
      });
      count++;
    }
  }

  console.log(`‚úÖ Seeded ${count} executives`);
  return count;
}
